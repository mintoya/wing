function levenshteinDistance(str1, str2) {
  let bias = 0;
  if (!str1 || !str2) {
    return 999;
  }
  const len1 = str1.length;
  const len2 = str2.length;
  if (!len1 || !len2) {
    return 999;
  }
  if (str1.includes(str2) || str2.includes(str1)) {
    bias -= 100 * Math.min(str1.length, str2.length);
  }
  let matrix = Array(len1 + 1)
    .fill(null)
    .map(() => Array(len2 + 1).fill(0));
  for (let i = 0; i <= len1; i++) {
    matrix[i][0] = i;
  }
  for (let j = 0; j <= len2; j++) {
    matrix[0][j] = j;
  }
  for (let i = 1; i <= len1; i++) {
    for (let j = 1; j <= len2; j++) {
      const cost = str1[i - 1] === str2[j - 1] ? 0 : 1;
      matrix[i][j] = Math.min(
        matrix[i - 1][j] + 1, // Deletion
        matrix[i][j - 1] + 1, // Insertion
        matrix[i - 1][j - 1] + cost, // Substitution
      );
    }
  }
  return matrix[len1][len2] + bias;
}
globalThis.fuzzySearch = (query, threshold = 10) => {
  globalThis.extendValidKeys();
  const data = validKeyValues.concat(tapDanceExtension);
  if (query == "" || query == null) {
    query = "A";
  }
  const results = [];
  const lowerCaseQuery = query.toLowerCase();
  for (const item of data) {
    const lowerCaseItem = item.name.toLowerCase();
    const lowerCaseItem2 = item.value.toLowerCase
      ? item.value.toLowerCase()
      : "";
    const distance = Math.min(
      levenshteinDistance(lowerCaseQuery, lowerCaseItem),
      levenshteinDistance(lowerCaseQuery, lowerCaseItem2),
    );
    if (distance <= threshold) {
      results.push({
        item: item,
        distance: distance,
      });
    }
  }
  results.sort((a, b) => a.distance - b.distance);
  return results;
};
globalThis.lookupKey = (input, type) => {
  globalThis.extendValidKeys();
  const all = validKeyValues.concat(tapDanceExtension);
  const isMatch = (valA, valB) => {
    if (valA === valB) return true;
    if (typeof valA === "object" && typeof valB === "object") {
      const vasonstr = (i) => {
        return globalThis.Vason.parse(JSON.stringify(i)).stringify()
          .toLowerCase();
      };
      return vasonstr(valA) === vasonstr(valB);
    }
    return false;
  };
  for (const key of all) {
    const target = key[type === "name" ? "value" : "name"];
    if (isMatch(target, input)) {
      return key[type];
    }
  }
  return type === "value" ? validKeyValues[0].value : validKeyValues[0].name;
};
